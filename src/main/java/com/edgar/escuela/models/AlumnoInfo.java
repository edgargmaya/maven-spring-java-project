package com.edgar.escuela.models;

public class AlumnoInfo {
	
	private String Matricula;
	private String Estado;
	private String Delegacion;
	private String Colinia;
	private String Calle;
	private String Numero;
	private String telefono;
	private String NombreTutor;
	private String ApellidoPTutor;
	private String ApellidoMTutor;
	private String Correo;
	private String Contrasena;
	
	public String getMatricula() {
		return Matricula;
	}
	public void setMatricula(String matricula) {
		Matricula = matricula;
	}
	
	public String getEstado() {
		return Estado;
	}
	public void setEstado(String estado) {
		Estado = estado;
	}
	
	public String getDelegacion() {
		return Delegacion;
	}
	public void setDelegacion(String delegacion) {
		Delegacion = delegacion;
	}
	
	public String getColinia() {
		return Colinia;
	}
	public void setColinia(String colinia) {
		Colinia = colinia;
	}
	
	public String getCalle() {
		return Calle;
	}
	public void setCalle(String calle) {
		Calle = calle;
	}
	
	public String getNumero() {
		return Numero;
	}
	public void setNumero(String numero) {
		Numero = numero;
	}
	
	public String getTelefono() {
		return telefono;
	}
	public void setTelefono(String telefono) {
		this.telefono = telefono;
	}
	
	public String getNombreTutor() {
		return NombreTutor;
	}
	public void setNombreTutor(String nombreTutor) {
		NombreTutor = nombreTutor;
	}
	
	public String getApellidoPTutor() {
		return ApellidoPTutor;
	}
	public void setApellidoPTutor(String apellidoPTutor) {
		ApellidoPTutor = apellidoPTutor;
	}
	
	public String getApellidoMTutor() {
		return ApellidoMTutor;
	}
	public void setApellidoMTutor(String apellidoMTutor) {
		ApellidoMTutor = apellidoMTutor;
	}
	
	public String getCorreo() {
		return Correo;
	}
	public void setCorreo(String correo) {
		Correo = correo;
	}
	
	public String getContrasena() {
		return Contrasena;
	}
	public void setContrasena(String contrasena) {
		Contrasena = contrasena;
	}

}
